    public void memberLoad()
    {
    //Code for loading table from database to jTable1
        int c;
        try {
            pst = con.prepareStatement("select * from members");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i = 1; i<=c; i++)
                {
                    v2.add(rs.getString("memberId"));
                    v2.add(rs.getString("memberName"));
                    v2.add(rs.getString("memberType"));
                    v2.add(rs.getString("gender"));
                    v2.add(rs.getString("joinDate"));
                    v2.add(rs.getString("emailID"));
                    v2.add(rs.getString("phoneno"));
                }
                d.addRow(v2);
            }
            pst=con.prepareStatement("select count(memberID) from members");
            ResultSet rs1 = pst.executeQuery();
            rs1.next();
            int count = rs1.getInt("count(memberID)");
            tfcount.setText(String.valueOf(count));
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(Members.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
