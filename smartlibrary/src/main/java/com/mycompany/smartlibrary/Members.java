    private void tfidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfidKeyReleased
        // The following code segment is for searching usinng memberID.
        try
        {
            String sql = "select * from members where memberID = ?";
            pst = con.prepareStatement(sql);
            pst.setString(1, tfid.getText());
            rs = pst.executeQuery();
            if(rs.next())
            {
                String s1 = rs.getString("memberName");
                String s2 = rs.getString("memberType");
                String s3 = rs.getString("gender");
                String s4 = rs.getString("joinDate");
                String s5 = rs.getString("emailID");
                String s6 = rs.getString("phoneno");
                tfname.setText(s1);
                tftype.setText(s2);
                tfgender.setText(s3);
                tfdate.setText(s4);
                tfemail.setText(s5);
                tfphone.setText(s6);
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_tfidKeyReleased

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      // Code for mouse click on jTable1 rows. 
        DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();       
        tfid.setText((d1.getValueAt(selectIndex, 0)).toString());
        tfname.setText((d1.getValueAt(selectIndex, 1)).toString());
        tftype.setText((d1.getValueAt(selectIndex, 2)).toString());
        tfgender.setText((d1.getValueAt(selectIndex,3)).toString());
        tfdate.setText((d1.getValueAt(selectIndex, 4)).toString());
        tfemail.setText((d1.getValueAt(selectIndex, 5)).toString());
        tfphone.setText((d1.getValueAt(selectIndex, 6)).toString());
        jButton1.setEnabled(false);  
    }//GEN-LAST:event_jTable1MouseClicked

